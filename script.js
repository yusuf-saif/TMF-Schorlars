// Initialization
function init() {
    populateDatalist();
    renderGroupProject();
    attachEventListeners();
}

// Populate the datalist for auto-suggestions
function populateDatalist() {
    elements.datalist.innerHTML = '';
    scholars.forEach(scholar => {
        const option = document.createElement('option');
        option.value = scholar.name;
        elements.datalist.appendChild(option);
    });
}

// Render the shared group project data
function renderGroupProject() {
    elements.groupTitle.textContent = groupProject.title;
    elements.groupSummary.textContent = groupProject.summary;
    
    elements.groupModules.innerHTML = '';
    groupProject.modules.forEach(module => {
        const li = document.createElement('li');
        li.textContent = module;
        elements.groupModules.appendChild(li);
    });
}

// Event Listeners
function attachEventListeners() {
    // Auto-fill track and skills if a known user is selected
    elements.nameInput.addEventListener('input', (e) => {
        const selected = scholars.find(s => s.name.toLowerCase() === e.target.value.toLowerCase());
        if (selected) {
            elements.trackInput.value = selected.track;
            // Join string array if it's an array
            elements.skillsInput.value = Array.isArray(selected.otherSkills) ? selected.otherSkills.join(", ") : selected.otherSkills;
            elements.trackInput.readOnly = true;
            elements.skillsInput.readOnly = true;
        } else {
            elements.trackInput.readOnly = false;
            elements.skillsInput.readOnly = false;
        }
    });

    elements.form.addEventListener('submit', handleFormSubmit);
    
    // Result actions
    elements.viewProjectBtn.addEventListener('click', openModal);
    elements.resetBtn.addEventListener('click', resetApp);
    
    // Modal actions
    elements.modalCloseBtn.addEventListener('click', closeModal);
    elements.modalCloseIcon.addEventListener('click', closeModal);
    elements.modal.addEventListener('click', (e) => {
        if (e.target === elements.modal) closeModal();
    });
    document.addEventListener('keydown', (e) => {
        if (e.key === 'Escape' && elements.modal.classList.contains('active-state')) {
            closeModal();
        }
    });

    // Copy / Share
    elements.copyBtn.addEventListener('click', copyProjectBrief);
    elements.shareBtn.addEventListener('click', shareProjectBrief);
    
    // Native share fallback hiding
    if (!navigator.share) {
        elements.shareBtn.style.display = 'none';
        elements.copyBtn.classList.remove('secondary-btn');
        elements.copyBtn.classList.add('primary-btn');
    }
}

// Form Submission & Loading Sequence
function handleFormSubmit(e) {
    e.preventDefault();
    
    const name = elements.nameInput.value.trim();
    const track = elements.trackInput.value.trim();
    const skills = elements.skillsInput.value.trim();

    if (!name || !track) return;

    // Direct Match Logic
    const matchedScholar = scholars.find(s => s.name.toLowerCase() === name.toLowerCase());
    currentProject = matchedScholar ? matchedScholar.project : fallbackProject;

    // Save for brief sharing
    currentScholarName = name;
    currentScholarTrack = track;
    currentScholarSkills = skills;

    // Transition to Loading State
    switchSections(elements.formSection, elements.loadingSection);
    elements.generateBtn.disabled = true;

    simulateLoading(() => {
        populateResultCard(name, track, skills);
        switchSections(elements.loadingSection, elements.resultSection);
        elements.generateBtn.disabled = false;
    });
}

// Progress Bar Animation
function simulateLoading(onComplete) {
    let progress = 0;
    elements.progressBar.style.width = '0%';
    elements.progressPercentage.textContent = '0%';

    const duration = 2500 + Math.random() * 1000;
    const intervalTime = 30;
    const steps = duration / intervalTime;
    const increment = 100 / steps;

    const interval = setInterval(() => {
        // Ensure smooth professional progress forward
        const randomBoost = Math.random() > 0.5 ? increment * 1.5 : increment * 0.5;
        progress += randomBoost;

        if (progress >= 100) {
            progress = 100;
            clearInterval(interval);
            setTimeout(onComplete, 300);
        }

        elements.progressBar.style.width = `${progress}%`;
        elements.progressPercentage.textContent = `${Math.floor(progress)}%`;
    }, intervalTime);
}

// View transitions
function switchSections(hideSection, showSection) {
    hideSection.classList.remove('active-state');
    setTimeout(() => {
        hideSection.classList.add('hidden-state');
        showSection.classList.remove('hidden-state');
        void showSection.offsetWidth;
        showSection.classList.add('active-state');
    }, 400); 
}

// Project populating
function populateResultCard(name, track, skills) {
    elements.resName.textContent = name;
    elements.resTrack.textContent = track;
    elements.resSkills.textContent = skills || 'N/A';
    
    elements.resTitle.textContent = currentProject.title;
    elements.resPriority.textContent = `${currentProject.priority} Priority`;
    
    elements.resPriority.className = 'badge';
    const priorityLower = currentProject.priority.toLowerCase();
    if (priorityLower === 'medium' || priorityLower === 'standard') elements.resPriority.classList.add('medium');
    else if (priorityLower === 'low') elements.resPriority.classList.add('low');
}

// Modal handling
function openModal() {
    // Populate Modal Content
    elements.modalTitle.textContent = currentProject.title;
    elements.modalOverview.textContent = currentProject.overview;
    elements.modalWhy.textContent = currentProject.whyThisProvesTrack;
    elements.modalProblem.textContent = currentProject.problem;
    elements.modalResearch.textContent = currentProject.researchRequirement;
    elements.modalOutcome.textContent = currentProject.expectedOutcome;
    
    // Toggle between requiredModules and coreTasks safely
    elements.modalTasksModules.innerHTML = '';
    let items = currentProject.requiredModules || currentProject.coreTasks || [];
    elements.tasksModulesTitle.textContent = currentProject.requiredModules ? 'Required Modules' : 'Core Tasks';
    
    items.forEach(obj => {
        const li = document.createElement('li');
        li.textContent = obj;
        elements.modalTasksModules.appendChild(li);
    });

    elements.modalDeliverables.innerHTML = '';
    currentProject.deliverables.forEach(del => {
        const li = document.createElement('li');
        li.textContent = del;
        elements.modalDeliverables.appendChild(li);
    });

    elements.modal.classList.remove('hidden-state');
    setTimeout(() => {
        elements.modal.classList.add('active-state');
        // Move focus inside the modal for accessibility
        elements.modal.focus();
    }, 10);
    
    document.body.style.overflow = 'hidden';
}

function closeModal() {
    elements.modal.classList.remove('active-state');
    setTimeout(() => {
        elements.modal.classList.add('hidden-state');
        document.body.style.overflow = '';
        // Return focus to trigger button
        elements.viewProjectBtn.focus();
    }, 300);
}

// Build text formatted for copying/sharing
function getBriefText() {
    let text = `SCHOLAR DETAILS:\n`;
    text += `Name: ${currentScholarName}\n`;
    text += `Track: ${currentScholarTrack}\n`;
    if (currentScholarSkills) text += `Skills: ${currentScholarSkills}\n`;
    text += `\n`;

    text += `PROJECT BRIEF: ${currentProject.title}\n`;
    text += `Priority: ${currentProject.priority}\n\n`;
    
    text += `OVERVIEW:\n${currentProject.overview}\n\n`;
    text += `WHY THIS PROVES THE TRACK:\n${currentProject.whyThisProvesTrack}\n\n`;
    text += `PROBLEM STATEMENT:\n${currentProject.problem}\n\n`;
    
    let tasksOrModules = currentProject.requiredModules || currentProject.coreTasks || [];
    let listTitle = currentProject.requiredModules ? 'REQUIRED MODULES' : 'CORE TASKS';
    text += `${listTitle}:\n`;
    tasksOrModules.forEach(o => text += `- ${o}\n`);
    text += `\n`;

    text += `RESEARCH REQUIREMENT:\n${currentProject.researchRequirement}\n\n`;

    text += `DELIVERABLES:\n`;
    currentProject.deliverables.forEach(d => text += `- ${d}\n`);
    text += `\n`;

    text += `EXPECTED OUTCOME:\n${currentProject.expectedOutcome}\n`;
    return text;
}

// Copy to Clipboard
async function copyProjectBrief() {
    const textToCopy = getBriefText();
    
    try {
        await navigator.clipboard.writeText(textToCopy);
        showToast("Project brief copied to clipboard!");
    } catch (err) {
        console.error('Failed to copy text: ', err);
        const textArea = document.createElement("textarea");
        textArea.value = textToCopy;
        document.body.appendChild(textArea);
        textArea.select();
        try {
            document.execCommand('copy');
            showToast("Project brief copied!");
        } catch (e) {
            alert("Failed to copy. Please manually select and copy the text.");
        }
        document.body.removeChild(textArea);
    }
}

// Share via Web Share API
async function shareProjectBrief() {
    if (navigator.share) {
        try {
            await navigator.share({
                title: `Scholar Project: ${currentProject.title}`,
                text: getBriefText(),
            });
            showToast("Project shared successfully!");
        } catch (err) {
            if (err.name !== 'AbortError') {
                console.error('Error sharing:', err);
                copyProjectBrief();
            }
        }
    } else {
        copyProjectBrief();
    }
}

// Toast notification
function showToast(message) {
    elements.toast.textContent = message;
    elements.toast.classList.add('show');
    
    setTimeout(() => {
        elements.toast.classList.remove('show');
    }, 3000);
}

// Reset App state
function resetApp() {
    elements.form.reset();
    
    // Explicitly clear readOnly attributes
    elements.trackInput.readOnly = false;
    elements.skillsInput.readOnly = false;

    // Reset progress visual state
    elements.progressBar.style.width = '0%';
    elements.progressPercentage.textContent = '0%';

    // Reset variables
    currentProject = null;
    currentScholarName = "";
    currentScholarTrack = "";
    currentScholarSkills = "";

    // Reset disabled states
    elements.generateBtn.disabled = false;

    switchSections(elements.resultSection, elements.formSection);
}

