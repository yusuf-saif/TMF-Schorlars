// Event Listeners
function attachEventListeners() {
    // Auto-fill track and skills if a known user is selected
    elements.nameInput.addEventListener('input', (e) => {
        const selected = scholars.find(s => s.name.toLowerCase() === e.target.value.toLowerCase());
        if (selected) {
            elements.trackInput.value = selected.track;
            // Join string array if it's an array
            elements.skillsInput.value = Array.isArray(selected.otherSkills) ? selected.otherSkills.join(", ") : selected.otherSkills;
            elements.trackInput.readOnly = true;
            elements.skillsInput.readOnly = true;
        } else {
            elements.trackInput.readOnly = false;
            elements.skillsInput.readOnly = false;
        }
    });

    elements.form.addEventListener('submit', handleFormSubmit);

    // Result actions
    elements.viewProjectBtn.addEventListener('click', openModal);
    elements.revealGroupBtn.addEventListener('click', startGroupProjectReveal);
    elements.resetBtn.addEventListener('click', resetApp);

    // Personal modal actions
    elements.modalCloseBtn.addEventListener('click', closeModal);
    elements.modalCloseIcon.addEventListener('click', closeModal);
    elements.modal.addEventListener('click', (e) => {
        if (e.target === elements.modal) closeModal();
    });

    // Group reveal and modal actions
    elements.groupRevealOverlay.addEventListener('click', (e) => {
        if (e.target === elements.groupRevealOverlay) cancelGroupReveal();
    });
    elements.groupModalCloseBtn.addEventListener('click', closeGroupProjectModal);
    elements.groupModalCloseIcon.addEventListener('click', closeGroupProjectModal);
    elements.groupProjectModal.addEventListener('click', (e) => {
        if (e.target === elements.groupProjectModal) closeGroupProjectModal();
    });

    document.addEventListener('keydown', handleGlobalKeydown);

    // Copy / Share
    elements.copyBtn.addEventListener('click', copyProjectBrief);
    elements.shareBtn.addEventListener('click', shareProjectBrief);
    elements.groupCopyBtn.addEventListener('click', copyGroupProjectBrief);
    elements.groupShareBtn.addEventListener('click', shareGroupProjectBrief);
}

function handleGlobalKeydown(e) {
    if (e.key !== 'Escape') return;

    if (isOverlayVisible(elements.groupRevealOverlay)) {
        cancelGroupReveal();
        return;
    }

    if (isOverlayVisible(elements.groupProjectModal)) {
        closeGroupProjectModal();
        return;
    }

    if (isOverlayVisible(elements.modal)) {
        closeModal();
    }
}

// Form Submission & Loading Sequence
function handleFormSubmit(e) {
    e.preventDefault();
    
    const name = elements.nameInput.value.trim();
    const track = elements.trackInput.value.trim();
    const skills = elements.skillsInput.value.trim();

    if (!name || !track) return;

    // Direct Match Logic
    const matchedScholar = scholars.find(s => s.name.toLowerCase() === name.toLowerCase());
    currentProject = matchedScholar ? matchedScholar.project : fallbackProject;

    // Save for brief sharing
    currentScholarName = name;
    currentScholarTrack = track;
    currentScholarSkills = skills;

    // Transition to Loading State
    switchSections(elements.formSection, elements.loadingSection);
    elements.generateBtn.disabled = true;

    simulateLoading(() => {
        populateResultCard(name, track, skills);
        switchSections(elements.loadingSection, elements.resultSection);
        elements.generateBtn.disabled = false;
    });
}

// Progress Bar Animation
function simulateLoading(onComplete) {
    let progress = 0;
    elements.progressBar.style.width = '0%';
    elements.progressPercentage.textContent = '0%';

    const duration = 2500 + Math.random() * 1000;
    const intervalTime = 30;
    const steps = duration / intervalTime;
    const increment = 100 / steps;

    const interval = setInterval(() => {
        // Ensure smooth professional progress forward
        const randomBoost = Math.random() > 0.5 ? increment * 1.5 : increment * 0.5;
        progress += randomBoost;

        if (progress >= 100) {
            progress = 100;
            clearInterval(interval);
            setTimeout(onComplete, 300);
        }

        elements.progressBar.style.width = `${progress}%`;
        elements.progressPercentage.textContent = `${Math.floor(progress)}%`;
    }, intervalTime);
}

// View transitions
function switchSections(hideSection, showSection) {
    hideSection.classList.remove('active-state');
    setTimeout(() => {
        hideSection.classList.add('hidden-state');
        showSection.classList.remove('hidden-state');
        void showSection.offsetWidth;
        showSection.classList.add('active-state');
    }, 400); 
}

